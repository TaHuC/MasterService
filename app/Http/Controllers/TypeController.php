     */
    public function store(Request $request)
    {
        //save in database
        $this->validate($request, [
            'title' => 'required|unique:types|string'
        ]);

        //Is valid
        $type = new Type();
        $type->title = $request->title;
        $type->save();

        return view('type.index');
    }

